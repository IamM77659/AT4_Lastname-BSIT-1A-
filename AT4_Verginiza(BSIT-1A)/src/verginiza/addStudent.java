    private void JBTNaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTNaddActionPerformed
        /*
            Attempts to check the file for existing data.  
            Retrieves the new username and password from input fields.  
            If both fields are empty, displays a warning message.  
            Otherwise, stores the new username and password in a JSON object.  
            Adds the new user to the user list and updates the record.  
            Saves the updated data to the file.  
            Closes the current window and opens the menu.  
            Logs any exceptions that occur.
        */

        try {
            filecheck();

            newname = namefield.getText();
            newpass = passField.getText();

            if (newname.isBlank() && newpass.isBlank()) {
                JOptionPane.showMessageDialog(null, "Please enter username and password.", "Message", JOptionPane.WARNING_MESSAGE);
            } else {
                
                use.put("username", newname);
                use.put("password", newpass);
                

                userlist.add(use);
                record.put("data", userlist);

                save();

                dispose();
                Menu a = new Menu();
                a.setVisible(true);

            
            }

        } catch (Exception ex) {
            Logger.getLogger(addStudent.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_JBTNaddActionPerformed

